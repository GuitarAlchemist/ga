namespace GA.Domain.Core.Theory.Harmony;

/// <summary>
///     Represents the harmonic function of an interval within a chord
/// </summary>
/// <remarks>
/// Expanded to include 9th, 11th, and 13th for jazz and extended harmony
/// </remarks>
public enum ChordFunction
{
    Root,
    Third,
    Fifth,
    Seventh,
    Ninth,
    Eleventh,
    Thirteenth
}
